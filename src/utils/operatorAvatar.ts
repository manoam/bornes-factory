/**
 * Helpers pour les avatars d'opérateurs quand on n'a pas (encore) la photo.
 * On affiche les initiales sur un fond coloré déterministe basé sur le
 * hash du nom — même couleur à chaque rendu pour le même nom.
 */

const PALETTE = [
  'bg-indigo-100 text-indigo-700',
  'bg-emerald-100 text-emerald-700',
  'bg-amber-100 text-amber-700',
  'bg-rose-100 text-rose-700',
  'bg-blue-100 text-blue-700',
  'bg-purple-100 text-purple-700',
  'bg-teal-100 text-teal-700',
  'bg-orange-100 text-orange-700',
];

function hashString(s: string): number {
  let h = 0;
  for (let i = 0; i < s.length; i++) {
    h = (h * 31 + s.charCodeAt(i)) | 0;
  }
  return Math.abs(h);
}

export function getOperatorInitials(name: string): string {
  return name
    .split(/\s+/)
    .filter(Boolean)
    .map((p) => p[0])
    .join('')
    .slice(0, 2)
    .toUpperCase();
}

export function getOperatorColor(name: string): string {
  return PALETTE[hashString(name) % PALETTE.length];
}
